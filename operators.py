"""This class defines some static methods for basic arithmetic operations."""


class Operators:

    @staticmethod
    def add(a, b):
        return a + b

    @staticmethod
    def sub(a, b):
        return a - b

    @staticmethod
    def mul(a, b):
        return a * b

    @staticmethod
    def div(a, b):
        return a // b

    @staticmethod
    def div_true(a, b):
        return a / b

    @staticmethod
    def mod(a, b):
        return a % b

    @staticmethod
    def exp(a, b):
        return a ** b
